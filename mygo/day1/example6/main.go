package main

import "fmt"

func main()  {
	var a int = 50
	var b bool
	c := 'a'
	fmt.Printf("%v\n",a)
	fmt.Printf("%v\n",b)
	fmt.Printf("%T\n",c)
	fmt.Printf("%T\n",c)
	fmt.Printf("90%%\n")
	fmt.Printf("%t\n",b)
	fmt.Printf("%b\n",32)
	fmt.Printf("%f\n",32.00)
	fmt.Printf("%x\n",15)
	fmt.Printf("%p\n",&a)
	var n int
	//输入赋值
	fmt.Scanf("%d",&n)
	fmt.Printf("%d\n",n)

	
}
